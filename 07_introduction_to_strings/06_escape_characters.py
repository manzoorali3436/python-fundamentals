"""
Python Escape Characters

Definition:
Escape characters are special characters that begin
with a backslash (\).

They allow you to insert special characters into a string.

Common Escape Characters:

\n  -> New line
\t  -> Horizontal tab
\\  -> Backslash
\'  -> Single quote
\"  -> Double quote
"""

# =====================================================
# Example 1
# =====================================================
# New line (\n)

print("Example 1")

print("Hello\nPython")

print("-" * 50)

# =====================================================
# Example 2
# =====================================================
